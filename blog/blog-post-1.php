            <h2>Ingredients You Will Need</h2>
            <ul>
                <li>All-purpose flour - 2 ½ cups</li>
                <li>Cocoa powder - 2 tbsp</li>
                <li>Baking soda - 1 tsp</li>
                <li>Salt - ½ tsp</li>
                <li>Butter - ½ cup (softened)</li>
                <li>Sugar - 1 ½ cups</li>
                <li>Oil - 1 cup</li>
                <li>Eggs - 2 (yolk and white separated)</li>
                <li>Curd - 1 cup</li>
                <li>Milk - ½ cup</li>
                <li>Red food coloring - 2 tbsp</li>
                <li>Vanilla essence - 1 tsp</li>
                <li>Vinegar - 1 tsp</li>
            </ul>

            <h2>Step-by-Step Preparation</h2>
            <ol>
                <li>Preheat the oven to 175°C (350°F) and line two 9-inch baking trays with parchment paper.</li>
                <li>In a bowl, mix flour, cocoa powder, baking soda, and salt. Set aside.</li>
                <li>In another bowl, beat butter and sugar until creamy. Add oil, egg yolks, vanilla essence, and vinegar. Mix well.</li>
                <li>Combine curd and milk, and add to the mixture.</li>
                <li>Add the dry ingredients to the wet mixture gradually, while mixing gently.</li>
                <li>Fold in the beaten egg whites to the batter and mix slowly.</li>
                <li>Add red food coloring and mix well until the color is even.</li>
                <li>Pour the batter into prepared baking trays and bake for 30 minutes.</li>
                <li>Let the cakes cool completely before frosting.</li>
